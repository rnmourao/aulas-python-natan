import random


def escolher_palavra(arquivo):
    # ler arquivo
    with open(arquivo, 'r') as arq:
        lista = arq.readlines()

    # escolher palavra aleatoriamente
    palavra = random.choice(lista)
    palavra = palavra.replace('\n', '')
    # devolver palavra
    return palavra

print(escolher_palavra('forca.txt'))

